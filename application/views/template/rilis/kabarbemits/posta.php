                                <h1 class="card-title mb-2">BEM ITS Ajak Mahasiswa ITS Tingkatkan Wawasan Internasional Melalui Kegiatan Global Movement</h1>
                                <small class="text-muted">Written by <strong>BEM ITS</strong>, posted Oktober 10, 2021</small>
                                <p class="card-text mt-2"><strong>BEM ITS, Surabaya -</strong> BEM ITS, Surabaya – Kepedulian dan kesadaran akan wawasan internasional merupakan aksi yang penting dalam kehidupan global saat ini. Untuk mewujudkan hal tersebut, Kementrian Hubungan Luar Badan Eksekutif Mahasiswa (BEM) ITS mengadakan kegiatan Global Movement. Dengan pedoman kata ‘Movement’ pada Global Movement, kegiatan ini diadakan dengan 2 rangkaian sub kegiatan sebagai bentuk aksi yang nyata.  Dua kegiatan tersebut adalah Webinar yang diadakan pada Sabtu, 30 Januari 2021 melalui platform youtube streaming dan yang kedua adalah Infographic Competition. Kegiatan di bawah naungan Kedirjenan Komunikasi dan Kerjasama Internasional ini memiliki tujuan menanamkan nilai internasionalisasi khususnya di lingkungan ITS. Namun, kegiatan Global Movement juga ini juga terbuka untuk umum. Global Movement mengangkat tema “The Stairway Toward Sustained Indonesia by 2030”. Tema ini didasarkan pada isu global terkini yang sedang digencar-gencarkan yaitu Sustainable Development Goals (SDG’s).  SDG’s merupakan perencanaan aksi global yang disepakati oleh para pemimpin dunia termasuk Indonesia dalam menjawab 17 tujuan dan 169 target. Dengan SDG’s ini, diharapkan mahasiswa ITS dan sekitarnya dapat menumbuh kan serta mengembangkan budaya internasionalisasi dan berkontribusi bagi Indonesia. Dari tema tersebut, Global Movement membawa 2 subtopik yaitu “Penanaman Mindset Industri Berkelanjutan untuk Sumber Daya Manusia pada Mahasiswa” yang dibawakan oleh Bapak Kemas Adrian dari Environmental and Sustainability Program Coordinator Pertamina Hulu Kalimantan Timur dan topik kedua adalah “Peran Pemuda sebagai Jembatan Kesenjangan Pendidikan di Indonesia” yang dibawakan oleh Ibu Dr. Agnes Tuti Rumiati, M.Sc sebagai Ketua SDG’s Center ITS serta Clara Lourdessa Oryza  selaku moderator yang memimpin jalannya acara ini.
                                </p>
